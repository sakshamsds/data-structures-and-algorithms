'''
121 -> _ _ _, 3 * 2 * 1 = 6
123 -> 3 * 2 * 1 = 6
n = 1, 6

121 + 123, 
121 -> 212, 213, 232, 312, 313,  3 * 121, 2 * 123
123 -> 312, 231, 232, 212, 2 * 121, 2 * 123
'''

class Solution:
    def numOfWays(self, n: int) -> int:
        a121, a123, MOD = 6, 6, 10 ** 9 + 7
        for i in range(n - 1):
            a121, a123 = (3 * a121 + 2 * a123) % MOD, (2 * a121 + 2 * a123) % MOD
        return (a121 + a123) % MOD

